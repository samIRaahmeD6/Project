          <p class="banner-description text-justify">
          United International University Debate Club (UIUDC) became the Unbeaten National Champion in Martyred Dr.
          Shamsuzzoha Memorial 6th Gold Bangladesh National Debate Fest 2022 by defeating Jahangirnagar University by 9-0 ballots.
          The Fest was held in Rajshahi University on 2-3 September 2022. A team of three members from UIUDC participated in this fest.
          They are M M Tasnim, Dept. of Economics, Abdullah Al Habib Badhon, Dept. of CSE and K. M. Ismail Safa, Dept. of Economics.
          Among them, Abdullah Al Habib Badhon became ‘Debater of the Tournament’ and ‘Debater of the Final’ at a time. <br><br>
          
          
          In this fest, 28 private and public Universities including Dhaka University, Jahangirnagar University, Jagannath University, Rajshahi University, Khulna University, Rajshahi College, East West University, Stamford University, Premier University, CUET, RUET participated in this national debate fest. 
          Professor Golam Shabbir Sattar, honorable Vice-Chancellor of Rajshahi University was the Chief Guest and handed over the Champion trophy to the winners.
          Professor Md. Sultan-Ul-Islam, Pro Vice-Chancellor of Rajshahi University was the Special Guest. Among others, Prof. Dr. Pradip Kumar Panday, Dept. of Mass Communication and Journalism, Prof. Dr. Md. Rabiul Islam, Dept. of Social Work, University of Rajshahi, Tasmia Haque, President of Gold Bangladesh and other prominent persons were also present there.
